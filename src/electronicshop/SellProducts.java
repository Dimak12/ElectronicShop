    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        ValidateInput val = new ValidateInput();
        if(!val.valPayment(payTF.getText())){
           
            JOptionPane.showMessageDialog(null, "Invalid payment entry");
        }
        else{
            
            double payment = Double.parseDouble(payTF.getText());
            double balance = payment - totDue;
            balTF.setText(""+balance);
            eShop.deduct(getSellTable());
            
        }
        
        
    }//GEN-LAST:event_confirmActionPerformed

