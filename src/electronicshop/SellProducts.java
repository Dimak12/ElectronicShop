    private void barcodeTF2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_barcodeTF2KeyPressed
    
        if(evt.getKeyCode()== KeyEvent.VK_ENTER){
            
            String query = "select * from stock where barcode = ?";
            eShop.readDatabase(barcodeTF2.getText().trim(),query,getBarcodeTF2(),getProductTF2(),getDescriptionTF2(),getQuantityTF2(),getPriceTF2());
            
        }
        else if(evt.getKeyCode()== KeyEvent.VK_BACKSPACE){
            
            productTF2.setText("");
            descriptionTF2.setText("");
            priceTF2.setText("");
            quantityTF2.setText("");
            
        }
    }//GEN-LAST:event_barcodeTF2KeyPressed

