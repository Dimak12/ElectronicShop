    public void fillSellTable (){
        
        double price = Double.parseDouble(priceTF2.getText());
        int qty = Integer.parseInt(quantityTF2.getText());
        double total = price*qty;
        
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)sellTable.getModel();
        Object [] elements = new Object[6];
        elements [0]= barcodeTF2.getText();
        elements [1]= productTF2.getText();
        elements [2]= descriptionTF2.getText();
        elements [3]= quantityTF2.getText();
        elements [4]= priceTF2.getText();
        elements [5]= total;
        model.addRow(elements);
        totDue = 0 ;
        for(int i=0; i<sellTable.getRowCount(); i++){
            
            totDue += Double.parseDouble(sellTable.getValueAt(i, 5).toString());
            
        }
        
        totDueTF.setText(""+totDue);
    }

